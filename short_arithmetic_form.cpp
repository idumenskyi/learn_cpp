#include <iostream>

using namespace std;

int main()
{
    int num = 1;
    cout << num << endl;
    num = num + 10;
    cout << num << endl;
    num = 1;
    cout << num << endl;
    num += 10;
    cout << num << endl;
    num *= 2;
    cout << num << endl;
    num -= 1;
    cout << num << endl;
    num /= 2;
    cout << num << endl;
    return 0;
}
